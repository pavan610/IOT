module.exports.text_to_braille=function (text){
    const brailee_dictionary = {    'a':[1,0,0,0,0,0],
                                    'b':[1,1,0,0,0,0],
                                    'c':[1,0,0,0,0,1],
                                    'd':[1,0,0,0,1,1],
                                    'e':[1,0,0,0,1,0],
                                    'f':[1,1,0,0,0,1],
                                    'g':[1,1,0,0,1,1],
                                    'h':[1,1,0,0,1,0],
                                    'i':[0,1,0,0,0,1],
                                    'j':[0,1,0,0,1,1],
                                    'k':[1,0,1,0,0,0],
                                    'l':[1,1,1,0,0,0],
                                    'm':[1,0,1,0,0,1],
                                    'n':[1,0,1,0,1,1],
                                    'o':[1,0,1,0,1,0],
                                    'p':[1,1,1,0,0,1],
                                    'q':[1,1,1,0,1,1],
                                    'r':[1,1,1,0,1,0],
                                    's':[0,1,1,0,0,1],
                                    't':[0,1,1,0,1,1],
                                    'u':[1,0,1,1,0,0],
                                    'v':[1,1,1,1,0,0],
                                    'w':[0,1,0,1,1,1],
                                    'x':[0,1,1,1,0,0],
                                    'y':[1,0,1,1,1,1],
                                    'z':[1,0,1,1,1,0],
                                    '#':[0,0,1,1,1,1]
                                }
    letters = [...text]
    braille = []
    letters.forEach(letter => {
        
        if(letter === ' '){
            letter = '#'
        }else{
            letter=letter.toLowerCase()
        }
        braille.push(brailee_dictionary[letter])
    });
    return braille
}
